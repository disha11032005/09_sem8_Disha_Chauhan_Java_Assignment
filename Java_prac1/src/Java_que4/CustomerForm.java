    private byte[] getPhotoBytes(File file) throws Exception {
    FileInputStream fis = new FileInputStream(file);
    ByteArrayOutputStream bos = new ByteArrayOutputStream();

    byte[] buffer = new byte[1024];
    int read;

    while ((read = fis.read(buffer)) != -1) {
        bos.write(buffer, 0, read);
    }

    fis.close();
    return bos.toByteArray();
}
    private void loadRecords() throws Exception {
    con = DBConnection.getConnection();
    st = con.createStatement(
        ResultSet.TYPE_SCROLL_INSENSITIVE,
        ResultSet.CONCUR_READ_ONLY
    );
    rs = st.executeQuery("SELECT * FROM Customer");
}


    private void displayRecord() throws Exception {
    txtName.setText(rs.getString("name"));
    txtHomeAddress.setText(rs.getString("home_address"));
    txtIncome.setText(rs.getString("annual_income"));
}

