    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {

        if (selectedFile == null) {
            JOptionPane.showMessageDialog(this, "Please upload photo first");
            return;
        }

        con = DBConnection.getConnection();

        String sql = "INSERT INTO Customer "
                + "(name, home_address, marital_status, annual_income, photo) "
                + "VALUES (?, ?, ?, ?, ?)";

        PreparedStatement ps = con.prepareStatement(sql);

        ps.setString(1, txtName.getText());
        ps.setString(2, txtHomeAddress.getText());
        ps.setString(3, rbMarried.isSelected() ? "Married" : "Unmarried");
        ps.setDouble(4, Double.parseDouble(txtIncome.getText()));
        ps.setBytes(5, getPhotoBytes(selectedFile)); // 🔥 PHOTO SAVED

        ps.executeUpdate();

        JOptionPane.showMessageDialog(this, "Record Saved Successfully");

        con.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnSaveActionPerformed

