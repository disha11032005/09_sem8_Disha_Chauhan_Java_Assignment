    private void btnavgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnavgActionPerformed
        try {
        con = DBConnection.getConnection();

        CallableStatement cs = con.prepareCall("{CALL avg_income_confirmed_married(?)}");
        cs.registerOutParameter(1, java.sql.Types.DOUBLE);
        cs.execute();

        double avg = cs.getDouble(1);
        jOptionPane1.showMessageDialog(this, "Average Income (Married): " + avg);

        con.close();
    } catch (Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnavgActionPerformed

